    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        if (txtNomeLogin.getText().equals("") || txtPassLogin.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Login ou Senha inválido.", "Erro ao efetuar o login!", JOptionPane.ERROR_MESSAGE);
        } else {

            try {
                Socket socket = new Socket("192.168.91.60", 59001); //INFOS DO SERVIDOR
                in = new Scanner(socket.getInputStream());
                out = new PrintWriter(socket.getOutputStream(), true);

                int cond = 0;

                //ENVIA A MENSAGEM AO SERVIDOR
                out.println("LOGIN_ALUNO" + txtNomeLogin.getText() + "‰" + txtPassLogin.getText());

                while (!out.equals("")) {///NAO SEI SE FAZ SENTIDO ESSA CONDICAO DO WHILE!!!!!!!!!!!!
                    cond = 1;
                    String input = in.nextLine();

                    if (input.equals("LOGIN_VALIDO")) {
                        JOptionPane.showMessageDialog(null, "Seja bem vindo: " + txtNomeLogin.getText(), "Login Concluido!", JOptionPane.INFORMATION_MESSAGE);
                        socket.close();
                        TelaAluno1 mc = new TelaAluno1(txtNomeLogin.getText());
                        mc.setDefaultCloseOperation(WindowConstants.DISPOSE_ON_CLOSE);
                        mc.setVisible(true);
                        this.setVisible(false);
                    } else {
                        JOptionPane.showMessageDialog(null, "Login ou Senha inválidos.", "Erro ao efetuar o login!", JOptionPane.ERROR_MESSAGE);
                        txtPassLogin.setText("");
                        break;
                    }
                }
                if (cond == 0) {
                    JOptionPane.showMessageDialog(null, "Login ou Senha inválidos.", "Erro ao efetuar o login!", JOptionPane.ERROR_MESSAGE);
                    txtPassLogin.setText("");
                }
            } //else do login e senha vazios
            catch (IOException ex) {
                JOptionPane.showMessageDialog(null, "Servidor fora do ar, aguarde...", "Erro ao efetuar o login!", JOptionPane.ERROR_MESSAGE);
                txtPassLogin.setText("");
                Logger.getLogger(LoginAluno1.class.getName()).log(Level.SEVERE, null, ex);
            }

        }
        txtNomeLogin.setText("");
        txtPassLogin.setText("");
    }//GEN-LAST:event_btnLoginActionPerformed

